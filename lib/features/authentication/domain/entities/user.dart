class User  {
  final int id;
  final String username;
  final String name;
  final String email;
  final String firstName;
  final String lastName;
  final String userType;

  const User({
    required this.id,
    required this.username,
    required this.name,
    required this.email,
    required this.firstName,
    required this.lastName,
    required this.userType,
  });
}